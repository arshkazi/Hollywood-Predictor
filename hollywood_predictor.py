import pandas as pd
import networkx as nx
import ast

# (Using cleaned_df from your previous cell)

print("="*60)
print("PART 6: BUILDING THE NETWORKX GRAPH")
print("="*60)

# Initialize an undirected graph
G = nx.Graph()

# Safety function to parse stringified lists safely (e.g. "['Actor A', 'Actor B']" -> ['Actor A', 'Actor B'])
def safe_parse_list(val):
    if pd.isna(val):
        return []
    if isinstance(val, list):
        return val
    try:
        # ast.literal_eval is safer than eval
        return ast.literal_eval(val)
    except:
        # If it's a raw comma-separated string instead of a Python list string
        return [item.strip() for item in str(val).split(',') if item.strip()]

# 1. Parse cast and director columns into clean Python lists
cleaned_df['cast_parsed'] = cleaned_df['cast'].apply(safe_parse_list)

# For directors, let's treat them as lists too in case a movie has co-directors
cleaned_df['director_parsed'] = cleaned_df['director'].apply(safe_parse_list)

# 2. Populate the graph with nodes and edges
for idx, row in cleaned_df.iterrows():
    movie_node = f"MOVIE_{row['title_clean']}"

    # Add the movie node
    G.add_node(movie_node, type='movie')

    # Add cast members and connect them to this movie
    for actor in row['cast_parsed']:
        actor_node = f"ACTOR_{actor.strip().lower()}"
        G.add_node(actor_node, type='actor')
        G.add_edge(movie_node, actor_node)

    # Add directors and connect them to this movie
    for director in row['director_parsed']:
        director_node = f"DIRECTOR_{director.strip().lower()}"
        G.add_node(director_node, type='director')
        G.add_edge(movie_node, director_node)

print("Graph constructed successfully!")
print(f"Total Nodes: {G.number_of_nodes()}")
print(f"Total Edges (Connections): {G.number_of_edges()}")

# Let's see some basic degree properties (who has the most direct connections?)
degrees = dict(G.degree())
# Filter degrees to only show actor/director nodes to see the busiest professionals
people_degrees = {k: v for k, v in degrees.items() if not k.startswith("MOVIE_")}
sorted_people = sorted(people_degrees.items(), key=lambda x: x[1], reverse=True)

print("\n" + "="*60)
print("MOST CONNECTED INDUSTRY PROFESSIONALS IN GRAPH (Direct Connections)")
