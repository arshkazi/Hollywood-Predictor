print("PART 7: CALCULATING PAGERANK STAR-POWER SCORES")
print("="*60)

# 1. Run PageRank on the entire graph G
pagerank_scores = nx.pagerank(G)

# Helper functions to calculate cumulative star power scores for each movie row
def get_cast_power(cast_list):
    total_power = 0.0
    for actor in cast_list:
        actor_node = f"ACTOR_{actor.strip().lower()}"
        # If the actor exists in our graph PageRank scores, add their score
        total_power += pagerank_scores.get(actor_node, 0.0)
    return total_power

def get_director_power(director_list):
    total_power = 0.0
    for director in director_list:
        director_node = f"DIRECTOR_{director.strip().lower()}"
        total_power += pagerank_scores.get(director_node, 0.0)
    return total_power

# 2. Map the calculated network scores back to our main DataFrame
cleaned_df['cast_star_power'] = cleaned_df['cast_parsed'].apply(get_cast_power)
cleaned_df['director_star_power'] = cleaned_df['director_parsed'].apply(get_director_power)

